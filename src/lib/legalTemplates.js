export const TEMPLATES = [TOS, FAQ, PRIVACY]

export function getTemplate(key) {
  return TEMPLATES.find(t => t.key === key) || null
}

// 依 store 算出變數初始值（合併預設值與自動帶入值）
export function initialVars(tpl, store) {
  const out = {}
  for (const varDef of tpl.variables) {
    const auto = typeof varDef.auto === 'function' ? varDef.auto(store) : undefined
    out[varDef.key] = (auto ?? '') || varDef.default || ''
  }
  return out
}

// 用目前變數渲染成 Markdown（缺的變數以初始值補齊）
export function renderTemplate(tpl, vars, store) {
  const base = initialVars(tpl, store)
  const merged = { ...base, ...(vars || {}) }
  return tpl.render(merged, store)
}
